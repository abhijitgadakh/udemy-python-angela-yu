def greet():
    print("Hello, Abhijit")
    print("How do you do, Abhijit?")
    print("Hey Abhijit, isn't the weather nice today?")

greet()


def greet_with_name(name):
    print(f"Hello {name}")
    print(f"How do you do, {name}?")
    print(f"Hey {name}, isn't the weather nice today?")


greet_with_name("Abhijit")